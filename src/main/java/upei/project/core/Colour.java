package upei.project.core;

/**
 * Represents the different property colors used in the game.
 * These colors are typically associated with property groups and
 * determine how players can build houses, hotels, or collect rent bonuses.
 */
public enum Colour {

    // Represents the blue property group
    Blue,

    // Represents the brown property group
    Brown,

    // Represents the light blue property group
    LightBlue,

    // Represents the red property group
    Red,

    // Represents the yellow property group
    Yellow,

    // Represents the green property group
    Green,

    // Represents the orange property group
    Orange,

    // Represents the purple property group
    Purple
}
